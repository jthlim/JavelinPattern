#include "Semaphore.h"

//===========================================================================

using namespace Javelin;

//===========================================================================

void Semaphore::Wait(size_t n) const
{
	for(size_t i = 0; i < n; ++i) Wait();
}

void Semaphore::Signal(size_t n) const
{
	for(size_t i = 0; i < n; ++i) Signal();
}

